def process():
    with open(DB_PATH, 'r', encoding='utf-8') as f:
        data = json.load(f)
        
    count = 0
    total_len = 0
    for cat in data['categories']:
        for subcat in cat.get('subcategories', []):
            for topic in subcat.get('topics', []):
                if topic.get('title') in TARGET_TITLES:
                    new_html = generate_html(topic.get('title', ''))
                    topic['fullContent'] = new_html
                    count += 1
                    total_len += len(new_html)
                    
    with open(DB_PATH, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        
    if count > 0:
        avg_len = total_len / count
        print(f"✅ 수정 완료: 총 {count}개 주제")
        print(f"📊 평균 글자수: {avg_len:.1f}자")
        print(f"검증: JSON 문법 정상")
    else:
        print("대상 주제를 찾지 못했습니다.")

if __name__ == '__main__':
    process()
